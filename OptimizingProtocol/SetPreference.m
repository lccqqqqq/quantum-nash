% setting up figure/font preferences


% Set default font size for axes and their labels/titles
set(groot, 'defaultAxesFontSize', 17);
set(groot, 'defaultAxesTitleFontSizeMultiplier', 1); % Multiplier for title font size
set(groot, 'defaultAxesLabelFontSizeMultiplier', 1); % Multiplier for label font size

% Set default font size for legends
set(groot, 'defaultLegendFontSize', 17);

% Set default font size for text objects in figures
set(groot, 'defaultTextFontSize', 17);

% Set default text interpreter to be Latex
set(groot, 'defaultTextInterpreter', 'latex');